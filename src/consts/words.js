const getWords = wordCount => {
  const selectedWords = {};
  while (wordCount > 0) {
    let wordIndex = Math.floor(15496 * Math.random());
    let word = words[wordIndex];
    if (!selectedWords[word]) {
      selectedWords[word] = 1;
      wordCount--;
    }
  }
  return Object.keys(selectedWords);
};

